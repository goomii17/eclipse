	public static ArrayList<Integer> dime_pos_aleatorias(int tamaño){
		
		ArrayList<Integer> posiciones=new ArrayList();
		
		for (int i = 0; i < tamaño; i++) {
			int n=(int) (Math.random()*tamaño);
			for (int j = 0; j < posiciones.size(); j++) {
				
				if(n==posiciones.get(j)) {
					
					n=(int) (Math.random()*tamaño);
					j=-1;
				}
				
			}
		
			posiciones.add(n);
			
		}
		return posiciones;
		
	}
	
